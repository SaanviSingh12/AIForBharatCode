private val translationMap: Map<String, Strings> = mapOf(
    "en" to en,
    "hi" to hi,
)

/**
 * Resolves translations for [languageCode].
 * Falls back: requested → Hindi → English.
 */
fun getStrings(languageCode: String): Strings {
    val short = languageCode.substringBefore("-")
    return translationMap[short] ?: translationMap["hi"] ?: en
}
